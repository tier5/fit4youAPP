package com.uipl.fitforyou.utility;

public class GymSetGet {

	public String id = "", gymname = "", gymaddress = "", current = "",
			city = "", state = "", zip = "";

	public GymSetGet(String id, String gymname, String gymaddress,
			String current, String city, String state, String zip) {

		this.id = id;
		this.gymname = gymname;
		this.current = current;
		this.gymaddress = gymaddress;
		this.city = city;
		this.state = state;
		this.zip = zip;

	}
}
